//Számok visszafelé 50-től 1-ig...
//for ciklussal
document.write("<br>");
for(let i=50; i>0;i--)
{
	document.write(i+",");
}
//while ciklussal
document.write("<br>");
let j=50;
while(j>0)
{
	document.write(j+",");
    j--;
}
document.write("<br>");
let k=50;
do
{
	document.write(k+",");
    k--;
}while(k>0);